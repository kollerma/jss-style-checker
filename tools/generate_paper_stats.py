REPO_ROOT = Path(__file__).resolve().parent.parent
CATALOGUE_YAML = REPO_ROOT / "specs" / "003-jss-rule-catalogue" / "catalogue.yaml"
HISTORY_DB = REPO_ROOT / "eval" / "precision-history.db"
MANIFEST_CSV = REPO_ROOT / "eval" / "corpus-manifest.csv"
GATE_EXCEPTIONS = REPO_ROOT / "eval" / "gate-exceptions.toml"
BENCHMARK_MD = REPO_ROOT / "eval" / "benchmark-results.md"
GOLD_SUMMARY_MD = REPO_ROOT / "eval" / "labeler-benchmark-summary.md"
NONR_SOURCES_MD = REPO_ROOT / "eval" / "non-cran-jss-sources.md"
OUTPUT_DIR = REPO_ROOT / "paper" / "generated"
PAPER_TEX = REPO_ROOT / "paper" / "paper.tex"

# --------------------------------------------------------------------------
# Pin constants — the ONLY values to touch when re-freezing the evaluation.
# The precision-history DB is append-only, so pinning an iteration label and
# a recall run timestamp makes regeneration byte-identical forever.
# --------------------------------------------------------------------------
PIN_ITERATION_LABEL = "post-code003-cli-flags"
PIN_RECALL_TS = "2026-07-11T21:23:39Z"
PIN_RECALL_HASH = "d5aab71bb82f97ee"
PIN_SCOPE = "full"
MIN_PLANTS = 10  # per-rule recall reported only at >= this many plants
PRECISION_GATE = 0.90
# Release-artifact facts (update at the pre-submission gate; see
# paper/revision-checklist.md items C1/C2).
REPO_URL = "https://github.com/kollerma/jss-style-checker-dev"
PYPI_NAME = "jss-style-checker"

_HEADER = (
    "%% AUTO-GENERATED by `python -m tools.generate_paper_stats`.\n"
    "%% Do not edit by hand. Pinned to precision iteration"
    f" `{PIN_ITERATION_LABEL}`\n"
    f"%% and recall run {PIN_RECALL_TS} (corpus {PIN_RECALL_HASH}).\n"
)


def _fmt_int(n: int) -> str:
    return f"{n:,}"


def _fmt_pct(x: float, digits: int = 1) -> str:
    return f"{100.0 * x:.{digits}f}\\%"


def _macro(name: str, value: str) -> str:
    return f"\\newcommand{{\\{name}}}{{{value}}}\n"


def _round_down(n: int, step: int = 50) -> int:
    return (n // step) * step


