_AUDIT_ALLOW = re.compile(
    r"^(19|20)\d{2}$"      # years
    r"|^[0-3]?\d$"          # small enumerations / section-ish numbers (0–39)
    r"|^(30|50|70|80|90)$"  # page/width/gate conventions spelled in prose
)


def audit_paper() -> int:
    if not PAPER_TEX.exists():
        print(f"error: {PAPER_TEX} not found", file=sys.stderr)
        return 2
    findings: list[tuple[int, str, str]] = []
    for lineno, raw in enumerate(PAPER_TEX.read_text(encoding="utf-8").splitlines(), 1):
        line = raw.split("%", 1)[0]
        if "\\Stat" in line or not line.strip():
            continue
        # skip pure-markup lines (labels, refs, geometry, includes)
        for num in re.findall(r"(?<![\w.\\{-])\d[\d,]*\.?\d*(?=\s*[%\\]?)", line):
            token = num.rstrip(".").replace(",", "")
            if not token or _AUDIT_ALLOW.match(token):
                continue
            findings.append((lineno, num, line.strip()[:90]))
    for lineno, num, ctx in findings:
        print(f"paper.tex:{lineno}: hard-coded number {num!r}: {ctx}")
    print(f"{len(findings)} finding(s)")
    return 1 if findings else 0


