"""Generate ``paper/generated/`` — every statistic the JSS paper cites.

Invoke from the repository root::

    python -m tools.generate_paper_stats          # write paper/generated/
    python -m tools.generate_paper_stats --check  # exit 1 if committed files drift
    python -m tools.generate_paper_stats --audit  # flag hard-coded numbers in paper.tex

No statistic in ``paper/paper.tex`` may be typed by hand: prose numbers
resolve through the ``\\Stat...`` macros in ``generated/stats.tex`` and
table bodies are ``\\input`` from ``generated/tab-*.tex``.  The single
source of truth is the pinned evaluation state below; the pin constants
are the only place to touch when re-freezing.

Follows the pattern of ``tools/generate_catalogue_data.py`` (AUTO-GENERATED
headers, atomic writes, ``--check`` drift gate).
"""

