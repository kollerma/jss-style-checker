def _fmt_int(n: int) -> str:
    return f"{n:,}"


def _fmt_pct(x: float, digits: int = 1) -> str:
    return f"{100.0 * x:.{digits}f}\\%"


def _macro(name: str, value: str) -> str:
    return f"\\newcommand{{\\{name}}}{{{value}}}\n"


def _round_down(n: int, step: int = 50) -> int:
    return (n // step) * step


