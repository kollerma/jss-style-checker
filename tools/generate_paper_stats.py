def render_stats(data: dict) -> str:
    cat = data["catalogue"]
    prec = data["precision"]
    rec = data["recall"]
    man = data["manifest"]
    nonr = data["nonr"]
    bench = data["benchmark"]
    exceptions = data["exceptions"]

    tp = sum(r[1] for r in prec["per_rule"])
    fp = sum(r[2] for r in prec["per_rule"])
    measured = len(prec["per_rule"])
    passing = sum(1 for _, t, f in prec["per_rule"] if t / (t + f) >= PRECISION_GATE)
    perfect = sum(1 for _, t, f in prec["per_rule"] if f == 0)

    rtp = sum(r[1] for r in rec["per_rule"])
    rfn = sum(r[2] for r in rec["per_rule"])
    scored = [(rid, t, f) for rid, t, f in rec["per_rule"] if t + f > 0]
    macro = sum(t / (t + f) for _, t, f in scored) / len(scored)
    thick = [(rid, t, f) for rid, t, f in scored if t + f >= MIN_PLANTS]
    perfect_recall = sum(1 for _, t, f in thick if f == 0)

    rnw = man["ext"].get("rnw", 0)
    rmd = man["ext"].get("rmd", 0)
    tex = man["ext"].get("tex", 0) + man["ext"].get("ltx", 0)

    out = [_HEADER, "%% Numbers — never type these by hand in paper.tex.\n"]
    m = out.append
    # Tool / repo facts
    m(_macro("StatToolVersion", data["tool_version"]))
    m(_macro("StatRuleCount", str(len(cat["active"]))))
    m(_macro("StatCategoryCount", str(len(cat["categories"]))))
    m(_macro("StatRetiredCount", str(len(cat["retired"]))))
    m(_macro("StatDeterministicCount", str(len(cat["deterministic"]))))
    m(_macro("StatSpecCount", str(data["n_specs"])))
    m(_macro("StatTestCount", _fmt_int(data["n_tests"])))
    m(_macro("StatCommitsApprox", _fmt_int(_round_down(data["n_commits"]))))
    m(_macro("StatAICommitsApprox", _fmt_int(_round_down(data["n_ai_commits"]))))
    # Corpus composition
    m(_macro("StatCorpusSize", str(prec["corpus_size"])))
    m(_macro("StatCorpusCran", str(man["source"].get("cran", 0))))
    m(_macro("StatCorpusGithub", str(man["source"].get("github", 0))))
    m(_macro("StatCorpusManual", str(man["source"].get("manual", 0))))
    m(_macro("StatCorpusRnw", str(rnw)))
    m(_macro("StatCorpusRmd", str(rmd)))
    m(_macro("StatCorpusTex", str(tex)))
    m(_macro("StatCorpusNonR", str(len(nonr["nonr"]))))
    langs = sorted(nonr["langs"], key=lambda k: (-nonr["langs"][k], k))
    m(_macro("StatCorpusNonRLangs", ", ".join(f"\\proglang{{{x}}}" for x in langs)))
    # Precision (pinned iteration)
    m(_macro("StatIterationCount", str(prec["n_iterations"])))
    m(_macro("StatPrecisionOverall", _fmt_pct(tp / (tp + fp))))
    m(_macro("StatPrecisionTP", _fmt_int(tp)))
    m(_macro("StatPrecisionFP", _fmt_int(fp)))
    m(_macro("StatPrecisionReviewed", _fmt_int(tp + fp)))
    m(_macro("StatRulesMeasured", str(measured)))
    m(_macro("StatRulesPassing", str(passing)))
    m(_macro("StatRulesPerfect", str(perfect)))
    m(_macro("StatGateExemptCount", str(len(exceptions))))
    m(_macro("StatGatePct", f"{PRECISION_GATE * 100:.0f}\\%"))
    # Recall (pinned run)
    m(_macro("StatRecallMicro", _fmt_pct(rtp / (rtp + rfn))))
    m(_macro("StatRecallMacro", _fmt_pct(macro)))
    m(_macro("StatRecallTP", _fmt_int(rtp)))
    m(_macro("StatRecallFN", _fmt_int(rfn)))
    m(_macro("StatRecallPlants", _fmt_int(rtp + rfn)))
    m(_macro("StatRecallRules", str(len(scored))))
    m(_macro("StatRecallThickRules", str(len(thick))))
    m(_macro("StatRecallPerfect", str(perfect_recall)))
    m(_macro("StatRecallMinPlants", str(MIN_PLANTS)))
    m(_macro("StatRecallCorpusPapers", str(data["n_recall_papers"])))
    # Labeling / methodology
    m(_macro("StatGoldSetSize", _fmt_int(data["gold_set_size"])))
    # Performance
    m(_macro("StatBenchPapers", str(bench["papers"])))
    m(_macro("StatBenchTotalSeconds", f"{bench['total_s']:.0f}"))
    m(_macro("StatBenchThroughput", f"{bench['throughput']:.1f}"))
    m(_macro("StatBenchMedianMs", f"{bench['metrics']['median'] * 1000:.0f}"))
    m(_macro("StatBenchPNinetyFiveMs", f"{bench['metrics']['p95'] * 1000:.0f}"))
    # Release artifacts (\urldef because \url{} cannot take a macro arg)
    m(f"\\urldef{{\\StatRepoUrl}}\\url{{{REPO_URL}}}\n")
    m(_macro("StatPyPIName", PYPI_NAME))
    # Provenance (printed in the reproducibility section)
    m(_macro("StatPinnedIteration", PIN_ITERATION_LABEL))
    m(_macro("StatPinnedIterationDate", prec["ts"].split("T")[0]))
    m(_macro("StatPinnedRecallRun", PIN_RECALL_TS.replace("T", " ").replace("Z", " UTC")))
    m(_macro("StatPinnedRecallHash", PIN_RECALL_HASH))
    return "".join(out)


def render_rule_summary(data: dict) -> str:
    """Per-category body rows: category, #rules, reviewed, precision."""
    cat = data["catalogue"]
    by_rule = {rid: (t, f) for rid, t, f in data["precision"]["per_rule"]}
    rows = []
    for category in cat["categories"]:
        rules = [r for r in cat["active"] if r["category"] == category]
        if not rules:
            continue
        tp = sum(by_rule.get(r["rule_id"], (0, 0))[0] for r in rules)
        fp = sum(by_rule.get(r["rule_id"], (0, 0))[1] for r in rules)
        prec = _fmt_pct(tp / (tp + fp)) if tp + fp else "---"
        pretty = category.replace("_", " ").capitalize()
        rows.append(
            f"{pretty} & {len(rules)} & {_fmt_int(tp + fp)} & {prec} \\\\\n"
        )
    # \bottomrule lives in the generated body: a booktabs rule may not
    # directly follow \input{} (misplaced \noalign).
    rows.append("\\bottomrule\n")
    return _HEADER + "".join(rows)


def render_precision_table(data: dict) -> str:
    exceptions = data["exceptions"]
    rows = []
    for rid, tp, fp in data["precision"]["per_rule"]:
        p = tp / (tp + fp)
        gate = "pass" if p >= PRECISION_GATE else (
            "exempt" if rid in exceptions else "FAIL"
        )
        rows.append(
            f"\\code{{{rid}}} & {_fmt_int(tp)} & {_fmt_int(fp)}"
            f" & {_fmt_pct(p)} & {gate} \\\\\n"
        )
    return _HEADER + "".join(rows)


def render_recall_table(data: dict) -> str:
    thick, thin_tp, thin_fn, thin_n = [], 0, 0, 0
    for rid, tp, fn in data["recall"]["per_rule"]:
        if tp + fn == 0:
            continue
        if tp + fn >= MIN_PLANTS:
            thick.append((rid, tp, fn))
        else:
            thin_tp, thin_fn, thin_n = thin_tp + tp, thin_fn + fn, thin_n + 1
    rows = [
        f"\\code{{{rid}}} & {_fmt_int(tp + fn)} & {_fmt_int(tp)}"
        f" & {_fmt_pct(tp / (tp + fn))} \\\\\n"
        for rid, tp, fn in thick
    ]
    if thin_n:
        rows.append("\\midrule\n")
        rows.append(
            f"{thin_n} rules below {MIN_PLANTS} plants (pooled)"
            f" & {_fmt_int(thin_tp + thin_fn)} & {_fmt_int(thin_tp)}"
            f" & {_fmt_pct(thin_tp / (thin_tp + thin_fn))} \\\\\n"
        )
    return _HEADER + "".join(rows)


def render_catalogue_table(data: dict) -> str:
    """Appendix body: every active rule with a \\hypertarget anchor."""
    cat = data["catalogue"]
    by_rule = {rid: (t, f) for rid, t, f in data["precision"]["per_rule"]}
    rows = []
    for category in cat["categories"]:
        rules = [r for r in cat["active"] if r["category"] == category]
        if not rules:
            continue
        pretty = category.replace("_", " ").capitalize()
        rows.append(
            f"\\multicolumn{{3}}{{l}}{{\\textbf{{{pretty}}}}} \\\\\n"
        )
        for r in sorted(rules, key=lambda r: int(r["rule_id"].rsplit("-", 1)[-1])):
            rid = r["rule_id"]
            desc = r["summary"] if "summary" in r else r["description"]
            desc = _tex_escape(desc.strip().rstrip("."))
            t, f = by_rule.get(rid, (0, 0))
            prec = _fmt_pct(t / (t + f)) if t + f else "---"
            rows.append(
                f"\\hypertarget{{rule:{rid}}}{{\\code{{{rid}}}}}"
                f" & {desc} & {prec} \\\\\n"
            )
        rows.append("\\addlinespace\n")
    return _HEADER + "".join(rows)


_TEX_ESCAPES = {
    "&": r"\&",
    "%": r"\%",
    "#": r"\#",
    "_": r"\_",
    "$": r"\$",
    "{": r"\{",
    "}": r"\}",
    "\\": r"\textbackslash{}",
    "~": r"\textasciitilde{}",
}


def _tex_escape(text: str) -> str:
    """Typeset a catalogue summary as plain text: macro mentions such as
    ``\\Address{}`` must be *displayed*, never executed."""
    text = re.sub(r'"([^"]*)"', r"``\1''", text)
    escaped = "".join(_TEX_ESCAPES.get(ch, ch) for ch in text)
    # Render macro mentions in code face: \textbackslash{}Foo -> \code{...}
    return re.sub(
        r"(\\textbackslash\{\}[A-Za-z@]+(?:\\\{\\\})?)",
        r"\\code{\1}",
        escaped,
    )


def render_benchmark_table(data: dict) -> str:
    b = data["benchmark"]
    rows = [
        f"Corpus size & {b['papers']} manuscripts \\\\\n",
        f"Total wall time (parse + lint) & {b['total_s']:.1f}\\,s \\\\\n",
        f"Throughput & {b['throughput']:.1f} manuscripts/s \\\\\n",
        f"Median per manuscript & {b['metrics']['median'] * 1000:.0f}\\,ms \\\\\n",
        f"95th percentile & {b['metrics']['p95'] * 1000:.0f}\\,ms \\\\\n",
        f"Maximum & {b['metrics']['max']:.2f}\\,s \\\\\n",
        "\\bottomrule\n",
    ]
    return _HEADER + "".join(rows)


