def collect() -> dict:
    db = sqlite3.connect(HISTORY_DB)
    try:
        precision = load_precision(db)
        recall = load_recall(db)
    finally:
        db.close()
    manifest = load_manifest()
    n_recall_papers = sum(
        1
        for p in (REPO_ROOT / "eval" / "recall-corpus").iterdir()
        if p.is_dir()
    )
    n_total, n_ai = count_commits()
    return {
        "catalogue": load_catalogue(),
        "precision": precision,
        "recall": recall,
        "manifest": manifest,
        "nonr": classify_nonr(manifest),
        "exceptions": load_gate_exceptions(),
        "benchmark": load_benchmark(),
        "gold_set_size": load_gold_set_size(),
        "n_tests": count_tests(),
        "n_commits": n_total,
        "n_ai_commits": n_ai,
        "n_specs": count_specs(),
        "n_recall_papers": n_recall_papers,
        "tool_version": tool_version(),
    }


def render_all(data: dict) -> dict[str, str]:
    return {
        "stats.tex": render_stats(data),
        "tab-rule-summary.tex": render_rule_summary(data),
        "tab-precision.tex": render_precision_table(data),
        "tab-recall.tex": render_recall_table(data),
        "tab-catalogue.tex": render_catalogue_table(data),
        "tab-benchmark.tex": render_benchmark_table(data),
    }


def _atomic_write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with tempfile.NamedTemporaryFile(
        "w", encoding="utf-8", delete=False, dir=path.parent, prefix=path.name + "."
    ) as tmp:
        tmp.write(content)
        name = tmp.name
    os.replace(name, path)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(prog="python -m tools.generate_paper_stats")
    parser.add_argument("--check", action="store_true")
    parser.add_argument("--audit", action="store_true")
    args = parser.parse_args(argv)

    if args.audit:
        return audit_paper()

    data = collect()
    if data["nonr"]["unknown"]:
        print(
            "warning: github corpus rows with no language classification in "
            f"{NONR_SOURCES_MD.name}: {', '.join(data['nonr']['unknown'])}",
            file=sys.stderr,
        )
    rendered = render_all(data)

    if args.check:
        stale = [
            name
            for name, content in rendered.items()
            if not (OUTPUT_DIR / name).exists()
            or (OUTPUT_DIR / name).read_text(encoding="utf-8") != content
        ]
        if stale:
            print(
                "error: paper/generated/ is out of date: " + ", ".join(stale),
                file=sys.stderr,
            )
            return 1
        return 0

    for name, content in rendered.items():
        _atomic_write(OUTPUT_DIR / name, content)
        print(f"wrote paper/generated/{name}")
    return 0


