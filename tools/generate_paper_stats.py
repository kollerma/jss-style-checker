def load_catalogue() -> dict:
    with CATALOGUE_YAML.open("r", encoding="utf-8") as f:
        doc = yaml.safe_load(f)
    retired = set(doc.get("retired_rule_ids") or ())
    active = [r for r in doc["rules"] if r["rule_id"] not in retired]
    return {
        "doc": doc,
        "active": active,
        "retired": sorted(retired),
        "deterministic": sorted(doc.get("deterministic_rule_ids") or ()),
        "categories": list(doc["categories"]),
    }


def load_precision(db: sqlite3.Connection) -> dict:
    row = db.execute(
        "SELECT id, ts, corpus_size, tool_version FROM iterations WHERE label = ?",
        (PIN_ITERATION_LABEL,),
    ).fetchone()
    if row is None:
        raise SystemExit(
            f"error: no iteration labelled {PIN_ITERATION_LABEL!r} in {HISTORY_DB}"
        )
    it_id, ts, corpus_size, tool_version = row
    per_rule = db.execute(
        "SELECT rule_id, tp, fp FROM iteration_rule_stats"
        " WHERE iteration_id = ? AND scope = ? AND tp + fp > 0"
        " ORDER BY rule_id",
        (it_id, PIN_SCOPE),
    ).fetchall()
    n_iterations = db.execute("SELECT COUNT(*) FROM iterations").fetchone()[0]
    return {
        "iteration_id": it_id,
        "ts": ts,
        "corpus_size": corpus_size,
        "tool_version": tool_version,
        "per_rule": per_rule,
        "n_iterations": n_iterations,
    }


def load_recall(db: sqlite3.Connection) -> dict:
    rows = db.execute(
        "SELECT rule_id, tp, fn FROM recall_history"
        " WHERE run_timestamp = ? AND corpus_hash = ? ORDER BY rule_id",
        (PIN_RECALL_TS, PIN_RECALL_HASH),
    ).fetchall()
    if not rows:
        raise SystemExit(
            f"error: no recall run at {PIN_RECALL_TS} / {PIN_RECALL_HASH} in {HISTORY_DB}"
        )
    return {"per_rule": rows}


def load_manifest() -> dict:
    with MANIFEST_CSV.open("r", encoding="utf-8", newline="") as f:
        rows = list(csv.DictReader(f))
    ext = Counter(
        Path(r["vignette_file"]).suffix.lower().lstrip(".") for r in rows
    )
    source = Counter(r["source"] for r in rows)
    return {"rows": rows, "ext": ext, "source": source}


def load_repo_langs() -> dict[str, tuple[str, bool]]:
    """repo -> (languages, non_r), from the "Corpus language classification"
    table in eval/non-cran-jss-sources.md (| Repo | Languages | Non-R |)."""
    langs: dict[str, tuple[str, bool]] = {}
    if not NONR_SOURCES_MD.exists():
        return langs
    in_section = False
    for line in NONR_SOURCES_MD.read_text(encoding="utf-8").splitlines():
        if line.startswith("## "):
            in_section = line.strip() == "## Corpus language classification"
            continue
        if not in_section or not line.strip().startswith("|"):
            continue
        cells = [c.strip() for c in line.strip().strip("|").split("|")]
        if len(cells) == 3 and "/" in cells[0] and cells[2] in ("yes", "no"):
            langs[cells[0]] = (cells[1], cells[2] == "yes")
    return langs


def classify_nonr(manifest: dict) -> dict:
    """Classify github-sourced corpus rows by manuscript language."""
    langs = load_repo_langs()
    nonr, r_lang, unknown = [], [], []
    lang_names: Counter[str] = Counter()
    for row in manifest["rows"]:
        if row["source"] != "github":
            continue
        entry = langs.get(row["source_id"])
        if entry is None:
            unknown.append(row["source_id"])
            continue
        lang, is_nonr = entry
        if is_nonr:
            nonr.append(row["source_id"])
            for part in lang.split("/"):
                if part.strip() != "R":
                    lang_names[part.strip()] += 1
        else:
            r_lang.append(row["source_id"])
    return {
        "nonr": sorted(nonr),
        "r": sorted(r_lang),
        "unknown": sorted(unknown),
        "langs": lang_names,
    }


def load_gate_exceptions() -> set[str]:
    with GATE_EXCEPTIONS.open("rb") as f:
        doc = tomllib.load(f)
    return {e["rule_id"] for e in doc.get("exceptions", ())}


def load_benchmark() -> dict:
    text = BENCHMARK_MD.read_text(encoding="utf-8")
    header = re.search(
        r"\*\*Papers:\*\*\s*(\d+)\s+\*\*Total wall \(parse\+lint\):\*\*\s*([\d.]+)\s*s"
        r"\s+\*\*Throughput:\*\*\s*([\d.]+)",
        text,
    )
    metrics = dict(re.findall(r"^\|\s*(mean|median|p95|min|max)\s*\|\s*([\d.]+)\s*\|", text, re.M))
    machine = re.search(r"^- Machine: (.+)$", text, re.M)
    if not header or "median" not in metrics:
        raise SystemExit(f"error: cannot parse {BENCHMARK_MD}")
    return {
        "papers": int(header.group(1)),
        "total_s": float(header.group(2)),
        "throughput": float(header.group(3)),
        "metrics": {k: float(v) for k, v in metrics.items()},
        "machine": machine.group(1).strip() if machine else "unknown",
    }


def load_gold_set_size() -> int:
    text = GOLD_SUMMARY_MD.read_text(encoding="utf-8")
    m = re.search(r"Gold set = \*\*([\d,]+) rows\*\*", text)
    if not m:
        raise SystemExit(f"error: cannot parse gold-set size from {GOLD_SUMMARY_MD}")
    return int(m.group(1).replace(",", ""))


def count_tests() -> int:
    out = subprocess.run(
        [sys.executable, "-m", "pytest", "--collect-only", "-q", "tests"],
        cwd=REPO_ROOT,
        capture_output=True,
        text=True,
        check=False,
    )
    m = re.search(r"(\d+) tests? collected", out.stdout + out.stderr)
    if not m:
        tail = (out.stdout + out.stderr).strip().splitlines()[-3:]
        raise SystemExit("error: pytest --collect-only failed: " + " / ".join(tail))
    return int(m.group(1))


def count_commits() -> tuple[int, int]:
    total = int(
        subprocess.run(
            ["git", "rev-list", "--count", "HEAD"],
            cwd=REPO_ROOT, capture_output=True, text=True, check=True,
        ).stdout.strip()
    )
    ai = int(
        subprocess.run(
            ["git", "rev-list", "--count", "--grep=Co-Authored-By: Claude", "HEAD"],
            cwd=REPO_ROOT, capture_output=True, text=True, check=True,
        ).stdout.strip()
    )
    return total, ai


def count_specs() -> int:
    specs = REPO_ROOT / "specs"
    return sum(1 for p in specs.iterdir() if p.is_dir() and re.match(r"\d{3}-", p.name))


def tool_version() -> str:
    m = re.search(
        r'^version = "([^"]+)"',
        (REPO_ROOT / "pyproject.toml").read_text(encoding="utf-8"),
        re.M,
    )
    assert m, "pyproject.toml version not found"
    return m.group(1)


